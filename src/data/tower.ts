function createTower(id: string, name: string, placements: TowerPlacement[], toursBeforeBoss?: number): TowerDefinition {
  const startingCellId = getCellId(id, startingCell.row, startingCell.col);
  const placementByPosition = new Map(placements.map((placement) => [getPositionKey(placement.row, placement.col), placement]));
  const cells: TowerCell[] = [];

  for (let row = 0; row < towerRows; row += 1) {
    for (let col = 0; col < towerColumns; col += 1) {
      const placement = placementByPosition.get(getPositionKey(row, col));
      cells.push({
        id: getCellId(id, row, col),
        label: placement?.label ?? (row === startingCell.row && col === startingCell.col ? 'Entree' : 'Salle vide'),
        kind: placement?.kind ?? 'empty',
        row,
        col,
        enemyCount: placement?.kind === 'enemy' ? placement.enemyCount ?? 1 : undefined
      });
    }
  }

  return { id, name, rows: towerRows, columns: towerColumns, startingCellId, cells, toursBeforeBoss };
}

function getCellId(towerId: string, row: number, col: number) {
  return `${towerId}-r${row}-c${col}`;
}

function getPositionKey(row: number, col: number) {
  return `${row}:${col}`;
}
